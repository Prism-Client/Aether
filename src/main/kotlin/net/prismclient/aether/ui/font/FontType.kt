package net.prismclient.aether.ui.font

/**
 * Represents the type of font within a font family. [FontStyle.fontFamily] must not be null for this to be functional.
 *
 * @see [FontStyle.fontType]
 * @see [UIFontFamily]
 *
 * @author sen
 * @since 1.0
 */
enum class FontType {
    Thin,
    ExtraLight,
    Light,
    Regular,
    Medium,
    SemiBold,
    Bold,
    ExtraBold,
    Black
}