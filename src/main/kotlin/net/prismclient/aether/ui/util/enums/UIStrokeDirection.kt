package net.prismclient.aether.ui.util.enums

/**
 * Instructs the renderer how to render the stroke. When center, the stroke is entered to the
 * outline of the given shape. Outside, is the outside of the shape, and inside is the inside of the shape.
 *
 * @author sen
 * @since 1.0
 */
enum class UIStrokeDirection {
    CENTER, OUTSIDE, INSIDE
}